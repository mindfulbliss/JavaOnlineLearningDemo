
public class ArthimeticOperations {

	public static void main (String[] args) {
		
		int m,n; 
		m=6;
		n=4;
		
		System.out.println("m+n ="+ (m+n));
		System.out.println("m-n ="+ (m-n));
		System.out.println("m*n ="+ m*n);
		System.out.println("m/n ="+ (double)m/n);
		System.out.println("m%n ="+ (m%n) );
		
		System.out.println("n++ = "+ n++);
		System.out.println("++n = "+ ++n);
	
	
		
		
	}
}
